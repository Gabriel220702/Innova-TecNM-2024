        <h4>¿Qué es Amoxtli-Jap?</h4>
        <p>Amoxtli-Jap es un avanzado dispositivo de monitoreo médico vestible, diseñado para el seguimiento de adultos mayores y personas con movilidad reducida. Integrado con sensores de alta precisión, permiten un monitoreo continuo y detallado del posicionamiento y movilidad del usuario. Su operatividad se fundamenta en la implementación de tecnología de vanguardia, incluyendo sensores de caída y algoritmos de procesamiento de datos, que permiten observar los movimientos indeseados en tiempo real. </p>
      </div>
      <div class="col-md-4">
        <h4>¿Qué ofrecemos?</h4>
        <p>Amoxtli-Jap ofrece un cinturón auxiliar con la integración de un dispositivo avanzado para la detección de caídas. Cuenta con dos diseños únicos y ergonómicos para ayudar y proporcionar traslados seguros para los usuarios por parte de la persona a cargo, así mismo el optimizar la capacidad de respuesta ante eventos críticos, minimizando el riesgo de peligros y lesiones graves derivadas de una caída.</p>
      </div>
      <div class="col-md-4">
        <h4>¿Porqué escojernos?</h4>
        <p>Amoxtli-Jap se presenta como una solución innovadora y proactiva ante la problematica mundial relacionada a las caídas. Diseñado con un enfoque en inclusión y equidad, el dispositivo garantiza que las personas mayores o con movilidad reducida reciban una asistencia de manera cómoda y segura a través de sus dos opciones, tal como el cinturón tipo faja o el cinturón tipo chaleco, ambos con implementación de sujetadores, además de una detección temprana y una respuesta efectiva ante caídas por medio de un sensor avanzado.</p>
